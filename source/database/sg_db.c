/*
 * Get any hints for the supplied ROM hash.
 */
uint16_t sg_db_get_hints (uint8_t *hash)
{

    for (uint32_t i = 0; i < (sizeof (sg_db) / sizeof (SG_DB_Entry)) ; i++)
    {
        if (memcmp (hash, sg_db [i].hash, HASH_LENGTH) == 0)
        {
            return sg_db [i].hints;
        }
    }

    /* No hints by default */
    return 0x0000;
}
